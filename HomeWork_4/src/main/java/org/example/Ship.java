package org.example;

public class Ship {
    private int high;
    private int width;

    public Ship(int high, int width) {
        this.high = high;
        this.width = width;
    }

    public Ship() {
        this.high = high;
        this.width = width;
    }

    public int getHigh() {
        return high;
    }

    public int getWidth() {
        return width;
    }
}
